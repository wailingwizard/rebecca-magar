                            <h3 class="h-h4">Education &amp; Background</h3>
                            <p>I kicked off my career with a temporary internship at WITF in Harrisburg, PA, where I designed advertisements and layouts for Central PA Magazine. This position allowed me to gain valuable experience in the field while completing my degree, but more importantly, it gave me a sense of purpose and a deeper understanding and appreciation for the broader impact that my work could have.</p>
                            <p>After graduation, I was hired by RoomMates decor in York, PA to illustrate products and produce packaging and collateral for the RoomMates brand. Painting and drawing were my first loves and influenced my decision to pursue graphic design as a career, so being able to work in a position where I could flex both my design and illustrative skills was a blast!</p>
                            <p>It was during my time here that I began to dabble in HTML. I saw a need for a skilled web designer at my job and curiosity took hold. I soon found myself with a raging desire to design and build websites. This desire led to a position working as a freelance front-end developer for IME-Tech. Here, I worked remotely with a team of developers to wireframe, design, and build custom SharePoint applications and sites for a variety of clients including Bank of America and Microsoft.</p>
                            <p>When the work at IME-Tech began to dry up in 2010, I branched out and established SG, LLC. During this time I worked for a variety of clients throughout the United States including Century Media Records, Bullet Tooth Records, Rizzetta & Company, SolarGaines, American Music Theatre, Re/Max, South Country Brewing, globalHMA, and many more.</p>
                            <p>My freelance work spanned a variety of skillsets with my primary focus being on developing custom WordPress themes and setting up WordPress sites. I eventually acquired a dedicated CentOS Linux server where I set up a shared hosting environment for my smaller clients. Here, I've been able to provide ongoing support and maintenance for their sites while maintaining meaningful relationships with a lot of wonderful people.</p>
                            <p>In December of 2011, I took on a temporary contract position with JPL in Harrisburg, PA where I worked closely with a team of developers to design and build mobile-friendly sites, HTML5 video players, and landing pages for The Hershey Company. These were early mobile sites—built just before responsive layouts took hold. There was an unlimited supply of Hershey's Kisses and team spirit at JPL and I learned so much from my time there.</p>
                            <p>Over the years, globalHMA remained a regular client. I continued to take on more duties at the company and ultimately transitioned to a full-time remote position in 2019. Here, I focused on design, development, and marketing for B2B clients in the heavy industry sector.</p>
                            <p>I wore a lot of hats at HMA. From graphic design to front-end development, to video editing, to marketing automation and IT tasks — I was quick to jump in wherever I was needed. My coworkers often told me, "you can do anything!" A willingness to try new things has been paramount to my success working independently and at a small agency where clients regularly introduce new software and systems (and where the ability to be fast, flexible, and versatile is key).</p>
                            <p>In May of 2022, I accepted a position as a Senior Web Designer at PNY Technologies. Here, I work with pro and consumer marketing teams to create web layouts, digital assets, and marketing materials for PNY and NVIDIA products including graphics cards, USB flash drives, memory cards, SSDs, and computer peripherals.</p>
